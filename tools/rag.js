// ============================================
// 檔案掃描
// ============================================
async function scanFiles(dirPath, extensions) {
  const results = [];

  async function walk(dir) {
    let entries;
    try {
      entries = await fs.readdir(dir, { withFileTypes: true });
    } catch {
      return;
    }

    for (const entry of entries) {
      const fullPath = path.join(dir, entry.name);

      if (entry.isDirectory()) {
        if (!SKIP_DIRS.has(entry.name)) {
          await walk(fullPath);
        }
      } else if (entry.isFile()) {
        const ext = path.extname(entry.name).toLowerCase();
        if (extensions.includes(ext)) {
          // 檔名層級過濾：.min.js, .bundle.css 等
          if (SKIP_FILE_PATTERNS.some((re) => re.test(entry.name))) continue;
          try {
            const stat = await fs.stat(fullPath);
            if (stat.size <= MAX_FILE_SIZE) {
              results.push({ fullPath, size: stat.size, mtime: stat.mtimeMs });
            }
          } catch {
            // skip unreadable files
          }
        }
      }
    }
  }

  await walk(dirPath);
  return results;
}

// ============================================
// 切片（Chunking）
// ============================================
function chunkFile(content, relPath, chunkLinesCount = CHUNK_LINES, chunkOverlap = CHUNK_OVERLAP) {
  const lines = content.split(/\r?\n/);
  const chunks = [];

  // 偵測 PHP function 定義位置（用於 function-level metadata）
  const funcDefs = detectPhpFunctions(lines);

  if (lines.length <= chunkLinesCount) {
    // 小檔案不切片
    const meta = buildChunkMeta(relPath, 1, lines.length, funcDefs);
    chunks.push({
      text: content,
      startLine: 1,
      endLine: lines.length,
      ...meta,
    });
    return chunks;
  }

  let start = 0;
  while (start < lines.length) {
    let end = Math.min(start + chunkLinesCount, lines.length);

    // 智慧邊界：如果切到函式中間，往後找空行或頂層 }
    if (end < lines.length) {
      const searchLimit = Math.min(end + 10, lines.length);
      for (let i = end; i < searchLimit; i++) {
        const line = lines[i].trim();
        if (line === "" || line === "}" || line === "};") {
          end = i + 1;
          break;
        }
      }
    }

    const slicedLines = lines.slice(start, end);
    const meta = buildChunkMeta(relPath, start + 1, end, funcDefs);
    chunks.push({
      text: slicedLines.join("\n"),
      startLine: start + 1,
      endLine: end,
      ...meta,
    });

    // 下一個 chunk 起始點（含重疊）
    start = end - chunkOverlap;
    if (start <= chunks[chunks.length - 1].startLine - 1) {
      start = end; // 防止無限迴圈
    }
  }

  return chunks;
}

/** 偵測 PHP 檔案中的 class 名稱和 function 定義（行號範圍） */
function detectPhpFunctions(lines) {
  let className = "";
  const funcs = []; // { name, startLine (1-based) }

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    const classMatch = line.match(/^\s*(?:abstract\s+)?class\s+(\w+)/);
    if (classMatch) className = classMatch[1];

    const funcMatch = line.match(/^\s*(?:public|protected|private)?\s*(?:static\s+)?function\s+(\w+)\s*\(/);
    if (funcMatch) {
      funcs.push({ name: funcMatch[1], startLine: i + 1 });
    }
  }

  return { className, funcs };
}

/** 為 chunk 附加 function-level metadata */
function buildChunkMeta(relPath, startLine, endLine, funcDefs) {
  const meta = {};

  // class name
  if (funcDefs.className) {
    meta.class_name = funcDefs.className;
  }

  // 找出此 chunk 包含的 function 名稱
  const containedFuncs = funcDefs.funcs
    .filter(f => f.startLine >= startLine && f.startLine <= endLine)
    .map(f => f.name);

  if (containedFuncs.length > 0) {
    meta.methods = containedFuncs.join(",");
  }

  // 檔案類型推斷（admin/model/ajax/frontend）
  const rp = relPath.replace(/\\/g, "/").toLowerCase();
  if (rp.includes("admincontrol") || rp.includes("/admin/")) {
    meta.file_type = "admin";
  } else if (rp.includes("cls/model") || rp.includes(".class.php")) {
    meta.file_type = "model";
  } else if (rp.includes("/ajax/") || rp.includes("_ajax")) {
    meta.file_type = "ajax";
  } else if (rp.includes("/js/") || rp.endsWith(".js")) {
    meta.file_type = "js";
  }

  return meta;
}

/** 取得相對於 basePath 的路徑 */
function getRelPath(fullPath) {
  const base = CONFIG.basePath.replace(/\\/g, "/").replace(/\/$/, "");
  const full = fullPath.replace(/\\/g, "/");
  return full.startsWith(base + "/") ? full.slice(base.length + 1) : full;
}

/** 從副檔名推斷語言 */
function detectLanguage(filePath) {
  const ext = path.extname(filePath).slice(1).toLowerCase();
  const map = {
    php: "php", js: "javascript", ts: "typescript", jsx: "javascript",
    tsx: "typescript", css: "css", html: "html", vue: "vue",
    sql: "sql", json: "json", md: "markdown", twig: "twig",
  };
  return map[ext] || ext;
}

// ============================================
// 並行控制
// ============================================
async function runWithConcurrency(tasks, concurrency) {
  const results = [];
  let index = 0;

  async function worker() {
    while (index < tasks.length) {
      const i = index++;
      results[i] = await tasks[i]();
    }
  }

  const workers = Array.from(
    { length: Math.min(concurrency, tasks.length) },
    () => worker()
  );
  await Promise.all(workers);
  return results;
}

/** 帶 timeout 的 Promise */
function withTimeout(promise, ms, label) {
  return Promise.race([
    promise,
    new Promise((_, reject) =>
      setTimeout(() => reject(new Error(`Timeout after ${ms}ms: ${label}`)), ms)
    ),
  ]);
}

